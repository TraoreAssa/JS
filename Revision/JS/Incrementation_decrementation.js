// -- Incrementation
var nb1 = 1;
nb1 = nb1 + 1; //J'augmentation de 1
// Affichage => nb1 = 2
console.log(nb1);

// Ecriture simplifiée
nb1++;
// Affichage => nb1 = 3
console.log(nb1);

// Ecriture simplifiée
++nb1;
// Affichage => nb1 = 4
console.log(nb1);


// -- Décrementation
nb1 = nb1 - 1; //Je retire 1
console.log(nb1);

// Ecriture simplifiée
nb1--;
console.log(nb1);

